#include "main.h"
#include <stdio.h>
#include <stdlib.h>
#include <ctype.h>
#include <string.h>
/**
*_strcat - concatenate two strings.
*@dest: destination string pointer.
*@src: source string pointer
* Return: pointer to destination string.
*/
char *_strcat(char *dest, char *src)
{
return strcat(dest, src);
}
